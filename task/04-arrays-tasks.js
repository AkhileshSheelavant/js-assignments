    var st=0,en=0;
    if(len==0)
        return arr;
    if(len%2==0)
    {
        st=0;
        en=Math.floor(len/2);
        var arr1=arr.splice(st,en);
        return arr.concat(arr1);

    }
    else
    {
        st=0;
        en=Math.floor(len/2);
        var arr1=arr.splice(st,en);
        var temp=arr.shift();
        arr.push(temp);
        return arr.concat(arr1);
    }

